#include<bits/stdc++.h>
using namespace std;
#define fast ios_base::sync_with_stdio(false);cin.tie(nullptr);cout.tie(nullptr);
#define ll long long
#define endl '\n'

void solve() {
    ll n, k;
    cin >> n >> k;
    vector<ll> divs;
    for (ll i = 1; i * i <= n; i++) {
        if (n % i == 0) {
            divs.push_back(i);
            if (i != n / i) divs.push_back(n / i);
        }
    }
    sort(divs.begin(), divs.end());
    if (k <= divs.size()) cout << divs[k - 1] << endl;
    else cout << -1 << endl;

    return;
}

int main() {
    fast;
    solve();
    return 0;
}