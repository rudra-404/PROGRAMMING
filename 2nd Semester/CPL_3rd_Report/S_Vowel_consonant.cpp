#include<bits/stdc++.h>
using namespace std;
#define fast ios_base::sync_with_stdio(false);cin.tie(nullptr);cout.tie(nullptr);
#define ll long long
#define endl '\n'

void solve(int x) {
    string s;
    getline(cin, s);
    string vowel, consonant;
    for (char c : s) {
        if (c == ' ') continue;
        if (tolower(c) == 'a' || tolower(c) == 'e' || tolower(c) == 'i' || tolower(c) == 'o' || tolower(c) == 'u') vowel += c;
        else consonant += c;
    }
    cout << "Case " << x << ":" << endl;
    cout << vowel << endl;
    cout << consonant << endl << endl;
}

int main() {
    fast;
    ll test;
    cin >> test;
    cin.ignore();
    for (int i = 1; i <= test; i++) solve(i);
    return 0;
}