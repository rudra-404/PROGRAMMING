int count_digit_fact(int n) {
    if (n == 1) return 1;
    double logSum = 0;
    for (int i = 1; i <= n; i++) {
        logSum += log10(i); 
    } return floor(logSum) + 1; 
}

void solve() {
    int n;
    cin >> n;
    cout << count_digit_fact(n) << endl;
}

