#include<bits/stdc++.h>
using namespace std;
#define fast ios_base::sync_with_stdio(false);cin.tie(nullptr);cout.tie(nullptr);
#define ll long long
#define endl '\n'

void solve() {
    int n;
    cin >> n;
    if (n == 1) {
        cout << "0" << endl;
        cout << "1" << endl;
        return;
    }
    int st = pow(2, n - 1);
    int end = pow(2, n) - 1;
    for (int i = st; i <= end; i++) {
        cout << bitset<32>(i).to_string().substr(32 - n) << endl;
    }
}

int main() {
    fast;
    ll test;
    cin >> test;
    while (test--) solve();
    return 0;
}