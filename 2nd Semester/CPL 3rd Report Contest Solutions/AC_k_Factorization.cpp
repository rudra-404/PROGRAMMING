#include<bits/stdc++.h>
using namespace std;
#define fast ios_base::sync_with_stdio(false);cin.tie(nullptr);cout.tie(nullptr);
#define ll long long
#define endl '\n'

void solve() {
    int n, k;
    cin >> n >> k;
    vector<int> factors;
    for (int i = 2; i * i <= n; i++) {
        while (n % i == 0) {
            factors.push_back(i);
            n /= i;
        }
    }
    if (n > 1) factors.push_back(n);
    if (factors.size() < k) cout << -1 << endl;
    else {
        while (factors.size() > k) {
            int l = factors.back();
            factors.pop_back();
            factors[factors.size() - 1] *= l;
        }
        for (int i = 0; i < factors.size(); i++) cout << factors[i] << " ";
        cout << endl;
    } return;
}

int main() {
    fast;
    solve();
    return 0;
}